public class Student {
    private int id;
    private String name;
    private  int[] marks;


    public void setMarks(int[] marks) {
        this.marks = marks;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int[] getMarks() {
        return marks;
    }


}

